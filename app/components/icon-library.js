/**
 * Get an icon SVG string with custom size and classes
 * @param {string} name - Icon name from Icons object
 * @param {number} size - Icon size in pixels (default: 20)
 * @param {string} className - Additional CSS classes
 * @returns {string} Complete SVG string
 */
export function getIcon(name, size = 20, className = "") {
  const iconSvg = Icons[name];
  if (!iconSvg) {
    console.warn(`Icon "${name}" not found in library`);
    return Icons.file; // Fallback to generic file icon
  }
  
  // Parse the SVG and add size/class attributes
  const parser = new DOMParser();
  const svgDoc = parser.parseFromString(iconSvg, 'image/svg+xml');
  const svg = svgDoc.documentElement;
  
  svg.setAttribute('width', size);
  svg.setAttribute('height', size);
  if (className) {
    svg.setAttribute('class', className);
  }
  
  return svg.outerHTML;
}

