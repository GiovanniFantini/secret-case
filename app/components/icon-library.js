/**
 * Get icon for a specific document type
 * @param {string} documentType - Document type identifier
 * @param {number} size - Icon size
 * @param {string} className - CSS classes
 * @returns {string} SVG icon
 */
export function getDocumentIcon(documentType, size = 20, className = "") {
  const iconName = DocumentTypeIcons[documentType] || 'document';
  return getIcon(iconName, size, className);
}

